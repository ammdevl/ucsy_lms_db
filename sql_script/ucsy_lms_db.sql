-- 1. Find the total number of students enrolled in each department and sort by highest enrollment?
SELECT d.department_name, COUNT(DISTINCT e.ykpt) AS total_students
FROM Departments d
JOIN Courses c ON d.department_id = c.department_id
JOIN Enrollments e ON c.course_id = e.course_id
GROUP BY d.department_id
ORDER BY total_students DESC;

-- 2. Check how many courses are currently 'In Progress' for 'Term 1'? 
SELECT COUNT(DISTINCT e.course_id) AS in_progress_count
FROM Enrollments e
JOIN Terms t ON e.term_id = t.term_id
WHERE e.completion_status = 'In Progress' 
AND t.semester_name = 'I';

-- 3. Find the number of late attendance records for each student? 
SELECT s.ykpt, concat(s.first_name,' ',s.middle_name,' ', s.last_name) as student_name, COUNT(a.attendance_id) AS late_count
FROM Students s
JOIN Attendance a ON s.ykpt = a.ykpt
JOIN Classes c ON a.class_id = c.class_id
WHERE a.time_arrived > c.start_time
GROUP BY s.ykpt;

-- 4. Retrieve the instructor with the highest number of assigned lectures and their details? 
SELECT i.*, COUNT(l.lecture_id) AS lecture_count
FROM Instructors i
JOIN Lectures l ON i.instructor_id = l.instructor_id
GROUP BY i.instructor_id
ORDER BY lecture_count DESC
LIMIT 1;

