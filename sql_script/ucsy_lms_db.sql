create database if not exists ucsy_lms_db;
use ucsy_lms_db;

-- Departments
create table if not exists Departments(
	department_id int primary key,
    department_name varchar(255) not null
);

-- Room
create table if not exists Room(
	room_id int primary key,
    room_name varchar(255) not null,
    building_name enum('A','B','C','D','E','F','Ext1','Ext2') not null
);

-- Terms
create table if not exists Terms(
	term_id int primary key,
    semester_name enum('I','II','III','IV','V','VI','VII','VIII','IX','X') not null,
    academic_year varchar(9) not null,
    is_active boolean not null default 0,
    start_date date not null,
    end_date date
);

-- Sections
create table if not exists Sections(
	section_id int primary key,
    term_id int not null,
    section_name varchar(255) not null,
    room_id int not null,
    foreign key (room_id) references Room(room_id),
    foreign key (term_id) references Terms(term_id)
);

-- Students
create table if not exists Students(
	ykpt int primary key,
    term_id int not null,
    first_name varchar(255) not null,
    middle_name varchar(255),
    last_name varchar(255) not null,
    dob date not null,
    edu_mail varchar(255) not null unique,
    telephone varchar(15) not null unique,
    password varchar(255) not null, 
    created_at timestamp default current_timestamp,
    foreign key (term_id) references Terms(term_id)
);

-- Parents
create table if not exists Parents(
	parent_id int primary key,
    ykpt int not null,
    father_name varchar(255) not null,
    mother_name varchar(255) not null,
    email varchar(255) not null,
    telephone varchar(15) not null,
    foreign key (ykpt) references Students(ykpt)
);

-- Instructors
create table if not exists Instructors(
	instructor_id int primary key,
    first_name varchar(255) not null,
    middle_name varchar(255),
    last_name varchar(255) not null,
    department_id int not null,
    edu_mail varchar(255) not null unique,
    telephone varchar(15) not null unique,
    password varchar(255) not null,
    created_at timestamp not null default current_timestamp,
    foreign key (department_id) references Departments(department_id)
);

-- Courses
create table if not exists Courses(
	course_id int primary key,
    term_id int not null,
    department_id int not null,
	course_name varchar(255) not null,
    course_description text,
    start_date date not null,
    end_date date,
    foreign key (department_id) references Departments(department_id),
    foreign key (term_id) references Terms(term_id)
);

-- Lectures
create table if not exists Lectures(
	lecture_id int primary key,
    term_id int not null,
    course_id int not null,
    instructor_id int not null,
    section_id int not null,
    lecture_name varchar(255) not null,
    lecture_description text,
    foreign key (term_id) references Terms(term_id),
    foreign key (course_id) references Courses(course_id),
    foreign key (instructor_id) references Instructors(instructor_id),
    foreign key (section_id) references Sections(section_id)
);

-- Classes
create table if not exists Classes(
	class_id int primary key,
    lecture_id int not null,
    room_id int not null,
    class_date date not null,
    start_time timestamp not null,
    end_time timestamp,
    foreign key (lecture_id) references Lectures(lecture_id),
    foreign key (room_id) references Room(room_id)
);


-- Enrollments
create table if not exists Enrollments(
	enrollment_id int primary key,
    ykpt int not null,
    term_id int not null,
    course_id int not null,
    enrolled_at timestamp not null default current_timestamp,
    completion_status enum('Not Started','In Progress','Completed') default 'Not Started',
    foreign key (term_id) references Terms(term_id),
    foreign key (ykpt) references Students(ykpt),
    foreign key (course_id) references Courses(course_id)
);

-- Assessments
create table if not exists Assessments(
	assessment_id int primary key,
    course_id int not null,
    assessment_type enum('Tutorial','Assignment','Lab Test','Quiz','Other Test') not null,
    assessment_name varchar(255) not null,
    assessment_description text,
    due_date datetime not null,
    assessment_score int not null,
    completion_status enum('Not Started','In Progress','Completed') default 'Not Started',
    foreign key (course_id) references Courses(course_id)
);

-- Announcements
create table if not exists Announcements(
	announcement_id int primary key,
    course_id int not null,
    instructor_id int not null,
    announcement_description text not null,
    announced_at timestamp not null,
    foreign key (course_id) references Courses(course_id),
    foreign key (instructor_id) references Instructors(instructor_id)
);

-- Attendance
create table if not exists Attendance(
	attendance_id int primary key,
    ykpt int not null,
    class_id int not null,
    time_arrived timestamp not null,
    time_left timestamp not null,
    foreign key (ykpt) references Students(ykpt),
    foreign key (class_id) references Classes(class_id)
);

-- Resources
create table if not exists Resources(
	resource_id int primary key,
    course_id int not null,
	resource_type enum('Video','Slides','TextBook','Supplement','Others') not null,
    url text not null,
    uploaded_time datetime not null,
    foreign key (course_id) references Courses(course_id)
);

-- Grades
create table if not exists Grades(
	grade_id int primary key,
    ykpt int not null,
    assessment_id int not null,
    earned_score int not null,
    feedback text,
    graded_at timestamp not null,
    foreign key (ykpt) references Students(ykpt),
    foreign key (assessment_id) references Assessments(assessment_id)
);

